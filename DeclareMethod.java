public class DeclareMethod {
    public static int sum(int a, int b) {
        return a + b; // Corrected sum logic
    }

    public static void main(String[] args) {
        int result = sum(5, 4); // Calling sum method
        System.out.println("Sum of 5 and 4 is: " + result); // Printing result
    }
}
